// 获取翻译文本的函数
export function getTranslation(key: TranslationKey, language: string = 'en', params?: Record<string, any>): string {
  // 如果不支持该语言，则回退到英语
  if (!translations[language]) {
    language = 'en';
  }
  
  // 获取请求的翻译文本
  let text = '';
  
  // 处理繁体中文特殊情况 - 如果zh_TW没有翻译，使用zh的翻译并转换
  if (language === 'zh_TW') {
    console.log(`[Translation Debug] Key: ${key}, has zh_TW translation: ${!!translations['zh_TW'][key]}`);
  }
  if (language === 'zh_TW' && (!translations['zh_TW'][key])) {
    // 如果繁体中文没有该键的翻译，使用简体中文，然后我们会手动转换一些常见字符
    text = translations['zh'][key] || translations.en[key] || key;
    
    // 一组基本的常用繁简字对照 - 只包含最常见的字符，避免重复
    const basicConversions = [
      ['说', '說'], ['语', '語'], ['个', '個'], ['还', '還'], ['发', '發'], 
      ['这', '這'], ['为', '為'], ['电', '電'], ['无', '無'], ['时', '時'],
      ['认', '認'], ['东', '東'], ['图', '圖'], ['书', '書'], ['头', '頭'],
      ['专', '專'], ['车', '車'], ['长', '長'], ['乡', '鄉'], ['见', '見'],
      ['风', '風'], ['云', '雲'], ['飞', '飛'], ['龙', '龍'], ['门', '門'],
      ['间', '間'], ['问', '問'], ['让', '讓'], ['们', '們'], ['么', '麼'],
      ['体', '體'], ['论', '論'], ['义', '義'], ['远', '遠'], ['乐', '樂'], 
      ['听', '聽'], ['国', '國'], ['医', '醫'], ['变', '變'], ['开', '開'], 
      ['马', '馬'], ['写', '寫'], ['学', '學'], ['动', '動'], ['运', '運'],
      ['会', '會'], ['来', '來'], ['后', '後'], ['过', '過'], ['没', '沒'],
      ['现', '現'], ['虽', '雖'], ['习', '習'], ['内', '內'], ['样', '樣'],
      ['点', '點'], ['机', '機'], ['资', '資'], ['气', '氣'], ['员', '員'],
      ['几', '幾'], ['实', '實'], ['总', '總'], ['于', '於'], ['给', '給'],
      ['药', '藥'], ['里', '裡'], ['历', '歷'], ['团', '團'], ['观', '觀'],
      ['传', '傳'], ['广', '廣'], ['场', '場'], ['业', '業'], ['线', '線'],
      ['华', '華'], ['术', '術'], ['权', '權'], ['卫', '衛'], ['计', '計'],
      ['试', '試'], ['检', '檢'], ['测', '測'], ['消', '消'], ['息', '息'],
      ['验', '驗'], ['记', '記'], ['连', '連'], ['买', '買'], ['进', '進']
    ];
    
    // 执行简单的转换
    for (const [simplified, traditional] of basicConversions) {
      text = text.replaceAll(simplified, traditional);
    }
  } else {
    // 常规情况
    text = translations[language][key] || translations.en[key] || key;
  }
  
  // 如果有参数，替换文本中的占位符
  if (params) {
    Object.keys(params).forEach(param => {
      text = text.replace(`{${param}}`, params[param]);
    });
  }
  
  return text;
}

// 创建一个简单的翻译函数
export function createTranslator(language: string) {
  return (key: TranslationKey, params?: Record<string, any> | string) => {
    // If params is a string, use it as a fallback string if translation doesn't exist
    if (typeof params === 'string') {
      const result = getTranslation(key, language);
      return result === key ? params : result;
    }
    
    // Otherwise, use params as normal replacement values
    return getTranslation(key, language, params);
  };
}